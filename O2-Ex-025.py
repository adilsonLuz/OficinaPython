# visualizando fatia da lista
lista =  [ "a","b","c" ,"d", "e", "f", "g", "h", "i", "j"]

print("\n lista ")
print(lista)

print("\n lista - visualização de fatias - [1:] ")
print(lista[1:])

print("\n lista - visualização de fatias - [3:] ")
print(lista[3:])

print("\n lista - visualização de fatias - [:4] ")
print(lista[:4])

print("\n lista - visualização de nova lista - [2:8:2] ")
lista2 = lista[2:8:2]
print(lista2)



